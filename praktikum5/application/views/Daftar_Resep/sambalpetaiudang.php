    <h2>Sambal Petai Udang</h2>

    <p>Resep masakan Indonesia identik dengan pedas, apalagi yang ini, sambal petai udang.  Bagi Anda yang tidak begitu suka dengan pedas, boleh dikurangi cabainya.</p>

    <h3>Bahan-bahan:</h3>
    <li>350 gr udang. Dibersihkan, kupas kulitnya, dan biarkan ekornya utuh.</li>
    <li>100 gram petai, bersihkan, belah</li>
    <li>3 buah tomat ukuran sedang, buang bijinya dan potong</li>
    <li>2 sdm kecap manis
    <li>2 sdm santan kental</li>
    <li>1/2 sdt garam</li>
    <li>11 buah cabai rawit</li>
    <li>4 sdm minyak goreng</li>
    <li>1 sdm gula pasir</li>
    <li>150 ml cangkir air</li>
    <h3>Bumbu dihaluskan:</h3>

    <li>80 gram cabai merah</li>
    <li>11 buah bawang merah</li>
    <li>4 siung bawang putih</li>
    <li>2 butir kemiri</li>
    <h3>Cara membuat:</h3>

    <li>Goreng petai sampai warnanya berubah menjadi hijau cerah â€“ kurang lebih selama 1 menit. Angkat dan sisihkan.</li>
    <li>Tumis bumbu yang dihaluskan, masukkan 1 sdm gula pasir. Biarkan sekitar 2 atau 3 menit sampai bumbu mengeluarkan bau harum.</li></li>
    <li>Masukkan tomat, lalu masak selama 1 menit.</li>
    <li>Tuangkan santan dan biarkan sampai mendidih, kira-kira selama 3 menit.</li>
    <li>Masukkan kecap manis dan garam. Lanjutkan memasak selama sekitar 10-15 menit hingga tomat menjadi benar-benar hancur.</li>
    <li>Masukkan petai dan cabai rawit, masak selama 3 menit.</li>
    <li>Masukkan udang, masak hingga matang.</li>
    <li>Tuang dalam piring dan sajikan.</li>

