    <h2>Nasi Goreng Sosis</h2>

    <p>Nasi goreng adalah salah satu resep masakan Indonesia yang sangat populer.  Biasa menjadi menu sarapan di rumah karena ada sisa nasi kemarin malam.  Sering juga menjadi menu andalan makan malam ala kaki lima, bahkan menjadi salah satu menu favorit di restoran.
    Tahun 2014 kemarin nasi goreng menempati urutan pertama resep masakan Indonesia paling dicari di Google. Nasi goreng mempunyai banyak sekali versi, berikut salah satunya.</p>
   <h3>Bahan - Bahan</h3>
    <li>1 piring nasi putih</li>
    <li>2 buah sosis</li>
    <li>kecap manis</li>
    <li>kecap asin</li>
    <li>garam</li>
    <li>2 sdm mixed vegetables</li>
    <li>1 sdm bawang merah goreng</li>
    <li>mentega</li>
    <li>1 siung bawang putih</li>
    <li>1 butir telur</li>
    <li>kaldu bubuk (opsional)</li>

    <h3>Pelengkap:</h3>
    <li>Kerupuk</li>
    <li>Mentimun, potong-potong</li>

    <h3>Cara Membuat:</h3>
    <li>Geprek bawang putih lalu cincang dan potong sosis</li>
    <li>Kocok lepas telur</li>
    <li>Panaskan mentega hingga cair lalu masukkan telur, bawang putih dan sosis, tumis sebentar lalu masukkan nasi putih</li>
    <li>Tuang kecap manis dan kecap asin secukupnya lalu goreng lagi kemudian beri sedikit garam,  dan satu sendok makan kaldu bubuk</li>
    <li>Cicipi sedikit, jika kurang rasa bisa ditambah sedikit garam lagi.</li>
    <li>Jika dirasa sudah pas, tuang nasi ke piring dan beri bawang goreng</li>
    <li>Sajikan selagi hangat.</li>

