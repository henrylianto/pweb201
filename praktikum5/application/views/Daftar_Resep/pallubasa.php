    <h2>Pallu Basa</h2>

    <p>Pallu basa adalah makanan tradisional Makassar, Sulawesi Selatan. Seperti Coto Mangkasara (Coto Makassar),  Pallubasa juga terbuat dari jeroan (isi dalam perut) sapi atau kerbau. Namun kini kuliner ini telah berkembang menjadi  pallu basa ayam dan pallu basa kepala ikan. Proses memasaknya pun hampir sama dengan Coto Makassar yang  lebih terkenal, yakni jeroan direbus dalam waktu lama. Setelah matang, jeroan ditambah dengan daging itu diiris-iris,  kemudian ditaruh/dihidangkan dalam mangkuk yang lebih enak jika disajikan panas atau hangat.</p>

    <h3>Bahan-bahan:</h3>
    <li>1. Daging sapi - 250 gram</li>
    <li>2. Kaldu sapi - 600 ml</li>
    <li>3. Kelapa parut, sangrai lalu haluskan hingga berminyak - 30 gram</li>
    <li>4. Serai, memarkan - 1 batang</li>
    <li>5. Lengkuas, memarkan - 2 cm</li>
    <li>6. Gula merah - 1 sdt</li>
    <li>7. Kayu manis - 1 cm</li>
    <li>8. Cengkeh - 4 buah</li>
    <li>9. Air asam - 30 ml</li>
    <li>10. Garam - 1 sdt</li>
    <li>11. Penyedap rasa (opsional) - 1 sdt</li>
    <li>12. Minyak, untuk menumis - 3 sdm</li>

    <h3>Bumbu Halus:</h3>
      <li>1. Bawang putih - 5 siung</li>
      <li>2. Bawang merah - 8 butir</li>
      <li>3. Cabai keriting - 5 buah</li>
      <li>4. Jahe - 2 cm</li>
      <li>5. Pala - 1/2 biji</li>
      <li>6. Ketumbar - 1 sdt</li>
      <li>7. Jinten - 1/2 sdt</li>
      <li>8. Pelengkap:
      <li>9. Irisan daun bawang - secukupnya
      <li>10. Bawang goreng - secukupnya
      <li>11. Irisan jeruk nipis - secukupnya
      <li>12. Sambal - secukupnya
      <li>13. Kecap â€“ secukupnya

    <h3>Cara Membuat:</h3>
    <li>1. Rebus daging sapi hingga empuk. Angkat dan dinginkan, lalu potong-potong kotak. Sisihkan 600 ml air sisa  rebusan daging sebagai kaldu sapi.</li>
    <li>2. Panaskan minyak. Tumis bumbu halus bersama dengan gula merah, serai, lengkuas, kayu manis, cengkeh  hingga harum dan matang.</li>
    <li>3. Tuangkan air kaldu sapi. Aduk rata lalu masak dengan api kecil hingga mendidih.</li>
    <li>4. Masukkan air asam, garam, penyedap rasa, dan kelapa sangrai. Aduk rata dan masak hingga bumbu meresap  dan kuah mengental. Angkat.</li>
    <li>5. Siap disajikan.
    <h3>Tips:</h3>
    <li>1. Jika suka, anda bisa menggunakan jeroan sapi sesuai selera.</li>
    <li>2. Daging bisa dimasak menggunakan panci presto agar lebih cepat empuk.</li>
    <li>3. Pallu basa biasa disajikan dengan kuning telur ayam mentah.</li>

