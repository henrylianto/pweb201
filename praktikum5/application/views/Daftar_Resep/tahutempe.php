    <h2>Tahu Tempe Bacem</h2>

    <p>Tahu tempe bacem biasanya disajikan dengan sayur asem, sambal dan nasi putih.  Resep masakan Indonesia ini  enak juga digunakan bumbunya untuk ayam, tetapi sebaiknya memasaknya terpisah. Kalau sedang terburu-buru bisa saja dimasak bersamaan, tetapi tahunya dimasukkan belakangan agar tidak terlalu lunak atau hancur.</p>

    <h3>Bahan-Bahan</h3> 
    <li>400 gram tempe/tahu, potong sesuai selera</li>
    <li>800 ml air kelapa</li>
    <li>100 gram gula merah, sisir</li>
    <li>2 sdm kecap manis</li>
    <li>1 lembar daun salam</li>
    <li>1 cm lengkuas, memarkan</li>
    <li>½ sdt garam</li>
    <li>minyak untuk menggoreng</li>

    <h3>Haluskan :</h3>
    <li>6 butir bawang merah</li>
    <li>4 siung bawang putih</li>
    <li>½ sdm ketumbar</li>
    <li>3 batang serai, ambil putihnya</li>

    <h3>Cara Membuat:</h3>
    <li>Ungkep atau rebus tempe bersama air kelapa, gula merah, kecap manis, daun salam, lengkuas, garam, dan bumbu halus sampai matang dan meresap. Biarkan dingin.</li>
    <li>Panaskan minyak, goreng di atas api sedang hingga kecokelatan. Angkat, tiriskan dan hidangkan.</li>
