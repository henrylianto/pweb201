    <h2>Soto Ayam</h2>

    <p>Soto merupakan salah satu resep masakan Indonesia yang mempunyai banyak variasi,  soto ayam, soto daging, soto babat, soto sulung, soto Betawi, soto mie dan masih banyak lagi. Variasi tersebut bisa berdasarkan bahan/isi, tambahan bumbu atau tempat di mana resep itu berasal.
    Resep yang kami pilih adalah resep soto ayam bening yang biasa dimasak sebagai masakan rumahan keluarga Indonesia.</p>

    <h3>Bahan-bahan:</h3>
    <li>1/2 ekor ayam</li>
    <li>2 liter air</li>
    <li>250 gr tauge panjang, buang akarnya</li>
    <li>1 batang daun bawang, iris halus</li>
    <li>1 1/2 sdt garam</li>
    <li>1 sdt gula pasir</li>
    <li>3 lembar daun jeruk</li>
    <li>1 batang serai, digeprek</li>
    <li>2 ruas lengkuas, digeprek</li>
    <li>2 ruas jahe, digeprek</li>

    <h3>Bumbu halus :</h3>
    <li>1 sdt merica bulat</li>
    <li>8 butir bawangmerah</li>
    <li>4 siung bawang putih</li>
    <li>3 ruas kunyit</li>

    <h3>Pelengkap :</h3>
    <li>2 batang seledri iris halus</li>
    <li>1 buah tomat, iris</li>
    <li>bawang merah goreng</li>
    <li>jeruk nipis, belah</li>

    <h3>Cara Membuat:</h3>
    <li>Rebus ayam hingga ayam empuk. Kecilkan apinya.</li>
    <li>Tumis bumbu halus, daun jeruk, serai, lengkuas, dan jahe hingga keluar aromanya. Angkat dan masukkan ke dalam air rebusan ayam. Aduk rata dan masak hingga mendidih.</li>
    <li>Tambahkan garam, gula pasir, kaldu bubuk, daun bawang, dan tauge. Aduk rata dan masak sebentar lalu angkat.</li>
    <li>Sajikan dalam mangkuk dan beri bahan pelengkap.</li>
