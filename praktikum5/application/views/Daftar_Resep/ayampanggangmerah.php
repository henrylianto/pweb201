    <h2>Ayam Panggang Bumbu Merah</h2>

    <p>Resep masakan Indonesia ini sering ditemui di menu hantaran sebagai teman nasi dan urap (sayur dengan bumbu kelapa) serta mentimun.</p>

    <h3>Bahan Bahan :</h3>
    <li>1 ekor ayam negeri, potong 10 bagian</li>
    <li>Satu sendok teh air jeruk limau</li>
    <li>1 sendok teh garam</li>
    <li>3 sendok makan minyak untuk menumis</li>
    <li>2 lembar daun salam</li>
    <li>600 ml santan dari 1 butir kelapa parut</li>
    <li>2 cm lengkuas, memarkan</li>
    <li>1/2 sendok makan garam</li>
    <li>1 sendok makan gula merah</li>
    <li>1/4 sendok teh merica bubuk</li>

    <h3> Bumbu Halus :</h3>
    <li>8 butir bawang merah</li>
    <li>4 siung bawang putih</li>
    <li>4 buah cabai merah besar, bakar</li>
    <li>8 buah cabai merah keriting, bakar</li>
    <li>4 butir kemiri, sangrai</li>

    <h3>Cara Membuat:</h3>
    <li>Lumuri ayam dengan air jeruk limau dan garam. Bakar sampai setengah matang. Sisihkan.</li>
    <li>Panaskan minyak, tumis bumbu halus, daun salam, dan lengkuas sampai harum.</li>
    <li>Masukkan santan, garam, gula merah, dan merica bubuk. Aduk rata. Masak sampai mendidih.</li>
    <li>Masukkan ayam, kecilkan api. Masak sampai bumbu meresap dan semua bahan matang. Angkat.</li>
    <li>Bakar ayam sambil diolesi sisa bumbu sampai harum dan kecokelatan. Angkat. Sajikan.</li>
