    <h2>Rendang</h2>

    <p>Salah satu resep masakan Indonesia yang selalu membuat ketagihan adalah rendang Padang. Siapa yang tidak kenal dengan rendang daging yang satu ini, kita semua tentu sudah tahu betapa lezat dan harumnya masakan ini.
    Memasak rendang daging sebaiknya menggunakan daging sapi yang memiliki kualitas yang terbaik, dan pilihlah daging yang masih dalam keadaan segar. Mari kita coba resep masakan Indonesia yang sangat populer ini..</p>

    <h3>Bahan-bahan:</h3>
    <li>  500 gram daging sapi, potong kotak jadi 8 atau 10 bagian</li>
    <li>  600 ml santan kental, dari satu setengah butir kelapa</li>
    <li>  <li>  5 sdm minyak goreng</li>
    <li>  300 ml air</li>
    <li>  Bumbu halus:</li>
    <li>  10 buah cabai merah iris</li>
    <li>  6 butir bawah merah</li>
    <li>  3 siung bawah putih</li>
    <li>  3 cm kunyit</li>
    <li>  3 cm jahe</li>
    <li>  3 cm lengkuas</li>
    <li>  1 sdm garam</li>
    <li>  2lembar daun kunyit</li>
    <li>  1 batang serai</li>
    <li>  2 butir asam kandis</li>

    <h3>Cara Membuat:</h3>
    <li>Tumis bumbu hingga harum, masukkan daging, aduk hingga berubah warna.</li>
    <li>Masukkan daun kunyit, serai, dan asam kandis. Aduk perlahan.</li>
    <li>Masukkan air. Masak hingga air menyusut. Aduk perlahan sesekali.</li>
    <li>Masukkan santan, masak dengan api sedang.</li>
    <li>  Biarkan hingga daging empuk, kuah kering, dan berminyak. Angkat, sajikan.</li>

