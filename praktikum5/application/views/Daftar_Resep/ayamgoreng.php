    <h2>Ayam Goreng</h2>

    <p>Ayam Goreng adalah Hidangan untuk setiap kala makanan disajikan, berikut merupakan resep yang kami andalkan dalam keseharian kami.</p>

    <h3>Bahan-bahan:</h3>
    <li>1 ekor ayam kampung muda, bagi menjadi 4 bagian</li>
    <li>1 buah jeruk nipis, peras airnya</li>
    <li>300 ml air kelapa muda</li>
    <li>2 lembar daun salam</li>
    <li>Minyak goreng secukupnya</li>

    <h3>Bumbu Halus:</h3>
    <li>5 siung bawang putih</li>
    <li>5 butir kemiri</li>
    <li>2 cm lengkuas</li>
    <li>2 cm kunyit</li>
    <li>garam</li>

    <h3>Cara Membuat:</h3>
    <li>Lumuri ayam dengan perasan air jeruk nipis.</li>
    <li>Tambahkan bumbu halus. Aduk rata, diamkan 30 menit</li>
    <li>Rebus ayam dalam air kelapa, tambahkan daun salam. Masak dengan api kecil dan aduk rata hingga ayam empuk, dan kuah mengental.</li>
    <li>Panaskan minyak banyak, goreng ayam hingga kuning kecoklatan.</li>
    <li>Angkat dan tiriskan.</li>
    <li>Hidangkan ayam goreng dan sambal, dilengkapi dengan lalapan segar.</li>

